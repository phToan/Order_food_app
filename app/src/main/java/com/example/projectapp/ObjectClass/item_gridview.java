package com.example.projectapp.ObjectClass;

import java.io.Serializable;

public class item_gridview implements Serializable {
    private String title;
    private int image;
    private int id;

    public item_gridview(int id, String title, int image) {
        this.id = id;
        this.title = title;
        this.image = image;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }

    public int getImage() {
        return image;
    }

    public void setImage(int image) {
        this.image = image;
    }
}
